-- Safe Supabase schema for Shanchuan Visual Archive.
-- Run in Supabase SQL Editor.
-- This creates tables, indexes, default data, and the public media bucket.
-- It intentionally does NOT grant anonymous write access.

create table if not exists public.settings (
  key text primary key,
  value text default '',
  updated_at timestamptz default now()
);

create table if not exists public.categories (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text not null unique,
  description text default '',
  cover_image text default '',
  sort_order integer default 0,
  is_primary boolean default true,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.projects (
  id bigint generated by default as identity primary key,
  title text not null,
  subtitle text default '',
  slug text not null unique,
  category_id bigint references public.categories(id) on delete set null,
  description text default '',
  cover_image text default '',
  year text default '',
  location text default '',
  is_featured boolean default false,
  is_recommended boolean default false,
  is_series boolean default false,
  series_style text default '',
  status text default 'draft' check (status in ('draft','published')),
  tags text default '',
  sort_order integer default 0,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.media (
  id bigint generated by default as identity primary key,
  project_id bigint references public.projects(id) on delete cascade,
  category_id bigint references public.categories(id) on delete set null,
  title text default '',
  description text default '',
  file_path text not null,
  storage_path text default '',
  original_name text default '',
  file_type text default '',
  mime_type text default '',
  size bigint default 0,
  media_type text default 'image',
  tags text default '',
  camera text default '',
  lens text default '',
  aperture text default '',
  shutter_speed text default '',
  iso text default '',
  captured_at text default '',
  is_hero boolean default false,
  is_selected boolean default false,
  is_cover boolean default false,
  show_in_database boolean default true,
  show_in_inspiration boolean default false,
  sort_order integer default 0,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.tags (
  id bigint generated by default as identity primary key,
  name text not null unique,
  slug text not null unique,
  created_at timestamptz default now()
);

create table if not exists public.project_tags (
  project_id bigint not null references public.projects(id) on delete cascade,
  tag_id bigint not null references public.tags(id) on delete cascade,
  primary key (project_id, tag_id)
);

create table if not exists public.media_tags (
  media_id bigint not null references public.media(id) on delete cascade,
  tag_id bigint not null references public.tags(id) on delete cascade,
  primary key (media_id, tag_id)
);

create index if not exists idx_categories_primary_sort on public.categories(is_primary, sort_order, id);
create index if not exists idx_projects_status_sort on public.projects(status, sort_order, id);
create index if not exists idx_projects_category on public.projects(category_id);
create index if not exists idx_projects_series on public.projects(is_series, is_recommended, sort_order);
create index if not exists idx_media_project on public.media(project_id, sort_order, id);
create index if not exists idx_media_category on public.media(category_id, sort_order, id);
create index if not exists idx_media_database on public.media(show_in_database, is_selected, sort_order, id);
create index if not exists idx_media_inspiration on public.media(show_in_inspiration, sort_order, id);
create index if not exists idx_media_hero on public.media(is_hero, updated_at desc, sort_order, id desc);

